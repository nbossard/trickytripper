package de.koelle.christian.trickytripper.model.modelAdapter;


public class PaymentInlineReport {
    
    private String total;
    private String payedBy;
    private String debitedTo;
    public String getTotal() {
        return total;
    }
    public void setTotal(String total) {
        this.total = total;
    }
    public String getPayedBy() {
        return payedBy;
    }
    public void setPayedBy(String payedBy) {
        this.payedBy = payedBy;
    }
    public String getDebitedTo() {
        return debitedTo;
    }
    public void setDebitedTo(String debitedTo) {
        this.debitedTo = debitedTo;
    }
    
    
    

}
